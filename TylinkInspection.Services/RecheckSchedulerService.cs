            string faultClosureStatus,
            string summary,
            DeviceInspectionResult? inspectionResult,
            string failureReason,
            string? playbackReviewSessionId)
        {
            return new TaskExecutionResult(
                Guid.NewGuid().ToString("N"),
                RecheckExecutionOutcomes.Failed,
                RecheckTaskStatuses.Failed,
                faultClosureStatus,
                summary,
                failureReason,
                inspectionResult,
                playbackReviewSessionId ?? string.Empty,
                ExecutionResultKind.Failed);
