        {
            return new TaskExecutionResult(
                Guid.NewGuid().ToString("N"),
                RecheckExecutionOutcomes.Completed,
                RecheckTaskStatuses.Completed,
                faultClosureStatus,
                summary,
                string.Empty,
                null,
                string.Empty,
                ExecutionResultKind.Completed);
        }
    }

